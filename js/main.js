function HadithChanger() {
  fetch("https://api.hadith.sutanlab.id/books/muslim?range=1-300")
    .then((e) => e.json())
    .then((e) => {
      let a = e.data.hadiths;
      function l() {
        (hadithContainer.innerText = a[hadithIndex].arab),
          (number.innerText = `300 - ${hadithIndex + 1}`);
      }
      l(),
        next.addEventListener("click", () => {
          299 == hadithIndex ? (hadithIndex = 0) : hadithIndex++, l();
        }),
        prev.addEventListener("click", () => {
          0 == hadithIndex ? (hadithIndex = 299) : hadithIndex--, l();
        });
    });
}
